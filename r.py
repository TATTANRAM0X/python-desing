def hangman(attemps):
    if attemps == 1:
        print(" ( ) ")
    if attemps == 2:
        print(" ( ) ")
        print("  |")
    if attemps == 3:
        print(" ( ) ")
        print("/ |")
    if attemps == 4:
        print(" ( ) ")
        print("/ | \\")
    if attemps == 5:
        print(" ( ) ")
        print("/ | \\")
        print("  |  ")
    if attemps == 6:
        print(" ( ) ")
        print("/ | \\")
        print("  |  ")
        print(" /")
    if attemps == 7:
        print(" ( ) ")
        print("/ | \\")
        print("  |  ")
        print(" / \\")
    if attemps == 8:
        print(" ( ) ")
        print("/ | \\")
        print("  |  ")
        print(" /'\ ")
